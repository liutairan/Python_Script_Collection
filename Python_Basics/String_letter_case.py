# Letter case related string operations

# upper
str1 = 'Test Test'
print(str1.upper())
# Output: 'TEST TEST'

# lower
str2 = 'Test Test'
print(str2.lower())
# Output: 'test test'

# title
str3 = 'Test test'
print(str3.title())
# Output: 'Test Test'

# capitalize
str4 = 'Test Test'
print(str4.capitalize())
# Output: 'Test test'
